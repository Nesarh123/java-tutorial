class Data_types {
    public static void main(String a[]){
        byte by = 127;
        short sh = 234;
        int num = 123;
        long l = 234l;

        float f = 123.34f;
        double d = 23.67;

        char c = 'f';

        boolean b = true;

        System.out.println(by);
        System.out.println(sh);
        System.out.println(num);
        System.out.println(l);

        System.out.println(f);
        System.out.println(d);

        System.out.println(c);

        System.out.println(b);

    }    
}
